package com.example.algorithm;

public class Caculator {

    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int divide(int a, int b) {
        return a / b;
    }

}