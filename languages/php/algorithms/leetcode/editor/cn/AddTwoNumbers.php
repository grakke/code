/**
 * Definition for a singly-linked list.
 * class ListNode {
 *     public $val = 0;
 *     public $next = null;
 *     function __construct($val = 0, $next = null) {
 *         $this->val = $val;
 *         $this->next = $next;
 *     }
 * }
 */
class Solution
{

    /**
     * @param  ListNode  $l1
     * @param  ListNode  $l2
     *
     * @return ListNode
     */
    public function addTwoNumbers($l1, $l2)
    {
        $newHead = new ListNode();
        $current = $newHead;

        $label = 0;
        while (!is_null($l1) || !is_null($l2)) {
            $l1val = $l1 ? $l1->val : 0;
            $l2val = $l2 ? $l2->val : 0;
            $sum = $l1val + $l2val + $label;

            $current->next = new ListNode($sum % 10);
            $label = (int) ($sum / 10);

            $l1 = $l1->next;
            $l2 = $l2->next;
            $current = $current->next;
        }
        // æœ€åŽæœ‰è¿›ä½
        if ($label) {
            $current->next = new ListNode($label);
        }

        return $newHead->next;
    }
}
