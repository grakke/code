class Solution
{

    /**
     * @param  Integer[]  $nums
     *
     * @return Integer
     */
    public function findMin($nums)
    {
        $low = 0;
        $high = count($nums) - 1;

        while ($low <= $high) {
            if ($high == $low) {
                return $nums[$low];
            }
            $mid = $low + floor(($high - $low) / 2);
            if (($mid == 0) && $nums[$mid] < $nums[$mid + 1] || ($nums[$mid] < $nums[$mid - 1])) {
                return $nums[$mid];
            }

            if ($nums[$mid] > $nums[$high]) {
                $low = $mid + 1;
            } else {
                $high = $mid - 1;
            }
        }
    }
}

