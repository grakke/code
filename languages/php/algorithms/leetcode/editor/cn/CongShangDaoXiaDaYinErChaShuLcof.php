use SplQueue;

/**
 * Definition for a binary tree node.
 * class TreeNode {
 *     public $val = null;
 *     public $left = null;
 *     public $right = null;
 *     function __construct($value) { $this->val = $value; }
 * }
 */
class Solution
{

    /**
     * @param TreeNode $root
     * @return Integer[]
     */
    public function levelOrder($root)
    {
        $res = [];
        if (is_null($root)) {
            return $res;
        }

        $queue = new SplQueue();
        while ($root || !$queue->isEmpty()) {
            if ($root) {
                $res[] = $root->val;
                $queue->enqueue($root->left);
                $queue->enqueue($root->right);
            }

            $root = $queue->dequeue();
        }

        return $res;
    }
}
